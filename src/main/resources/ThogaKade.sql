DROP DATABASE ThogaKade;
CREATE DATABASE ThogaKade;
USE ThogaKade;

CREATE TABLE `T_USER`(
	`USER_NAME` VARCHAR(50) UNIQUE,
	`PASSWORD` VARCHAR(500),
	 CONSTRAINT PRIMARY KEY (`USER_NAME`)
)
  ENGINE = InnoDB
  AUTO_INCREMENT = 1
  DEFAULT CHARSET latin1;

CREATE TABLE `T_CUSTOMER`(
	`ID` VARCHAR(10) NOT NULL,
	`NAME` VARCHAR(50) ,
	`ADDRESS` VARCHAR(100),
	`SALARY` double(10,2),
	 CONSTRAINT PRIMARY KEY (`ID`)
)
  ENGINE = InnoDB
  AUTO_INCREMENT = 1
  DEFAULT CHARSET latin1;

CREATE TABLE `T_ITEM`(
	`CODE` VARCHAR(10) NOT NULL,
	`NAME` VARCHAR(50) NOT NULL,
	`DESCRIPTION` VARCHAR(20),
	`UNIT_PRICE` DECIMAL(20,2) NOT NULL,
	`QTY_ON_HAND` INT(20) NOT NULL,
	CONSTRAINT PRIMARY KEY (`CODE`)
)
  ENGINE = InnoDB
  AUTO_INCREMENT = 1
  DEFAULT CHARSET latin1;

CREATE TABLE `T_ORDER`(
	`ID` VARCHAR(10) NOT NULL,
	`DATE` DATE,
	`CUST_ID` VARCHAR(10) NOT NULL,
	CONSTRAINT PRIMARY KEY (`ID`),
	CONSTRAINT FOREIGN KEY(`CUST_ID`) REFERENCES T_CUSTOMER(`ID`)
	ON UPDATE CASCADE ON DELETE CASCADE
)
  ENGINE = InnoDB
  AUTO_INCREMENT = 1
  DEFAULT CHARSET latin1;

CREATE TABLE `T_ORDER_DETAIL`(
	`ID` VARCHAR(10) NOT NULL,
	`ITEM_CODE` VARCHAR(10) NOT NULL,
	`QTY` INT(20) NOT NULL,
	CONSTRAINT PRIMARY KEY (`ID`,`ITEM_CODE`),
	CONSTRAINT FOREIGN KEY (`ID`) REFERENCES T_ORDER(`ID`)
	ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT FOREIGN KEY (`ITEM_CODE`) REFERENCES T_ITEM(`CODE`)
	ON UPDATE CASCADE ON DELETE CASCADE
)
  ENGINE = InnoDB
  AUTO_INCREMENT = 1
  DEFAULT CHARSET latin1;

